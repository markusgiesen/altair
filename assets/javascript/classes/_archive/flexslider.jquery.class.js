/**
 * Hook up Flexslider
 *
 * Needs jquery.min.js & spin.js
 * http://www.woothemes.com/flexslider/
 */

var flexslider = {

	/**
	 * Set options used in flexslider plugin
	 */
	opts: {
		namespace: '',                              // String: Prefix string attached to the class of every element generated by the plugin
		selector: '.slider__group > .slider__item', // Selector: Must match a simple pattern. '{container} > {slide}' -- Ignore pattern at
		slideshow: true,                            // Boolean: Animate slider automatically
		startAt: 0,                                 // Integer: The slide that the slider should start on. Array notation (0 = first slide)
		slideshowSpeed: 6000,                       // Integer: Set the speed of the slideshow cycling, in milliseconds
		animationSpeed: 1200,                       // Integer: Set the speed of animations, in milliseconds
		pauseOnAction: true,                        // Boolean: Pause the slideshow when interacting with control elements, highly recommended.
		pauseOnHover: true,                         // Boolean: Pause the slideshow when hovering over slider, then resume when no longer hovering
		controlNav: true,                           // Boolean: Create navigation for paging control of each clide? Note: Leave true for manualControls usage
		directionNav: false,                        // Boolean: Create navigation for previous/next navigation? (true/false)
		keyboard: false,                            // Boolean: Allow slider navigating via keyboard left/right keys
		animationLoop: true,                        // Boolean: Should the animation loop? If false, directionNav will received "disable" classes at either end
		start: function(slider_element){            // Callback: function(slider) - Fires when the slider loads the first slide
			$(slider_element).spin(false);          // Remove spinner
		}
	},

	/**
	 * Initiate the flexslide plugin
	 * @param {slider_element} a string for the element identifier to pass to the flexslider
	 */
	init: function(slider_element){
		if(slider_element) {
			$(slider_element).flexslider(this.opts);
		}
	}
};
